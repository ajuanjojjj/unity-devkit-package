using UnityEngine;




namespace PossumScream.Behaviours
{
	public abstract class SingletonPossumBehaviour<T> : InstantiablePossumBehaviour<T> where T : Component
	{
		#region Events


			protected void Awake()
			{
				if (m_instance == null) {
					m_instance = this as T;
				}
				else if (m_instance != this) {
					Destroy(this);
					return;
				}

				LateAwake();
			}


		#endregion
	}
}




